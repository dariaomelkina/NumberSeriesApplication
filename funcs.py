"""
Module with basic functions.
"""


def add(x, y):
    """ Addition """
    return x + y


def subtract(x, y):
    """ Subtraction """
    return x - y


def multiply(x, y):
    """ Multiplication """
    return x * y


def divide(x, y):
    """ Division """
    return x / y


def floor_divide(x, y):
    """ Floor division  """
    return x // y


def modulus(x, y):
    """ Modulus """
    return x % y


def exponent(x, y):
    """ Exponentiation """
    return x ** y
